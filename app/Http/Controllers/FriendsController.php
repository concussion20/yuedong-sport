     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        //
        $fds = Friend::where('fd_id','=',Auth::id())->
        orWhere('user_id','=',Auth::id())->get();

        $friends = array();
        foreach ($fds as $fd) {
            if(Auth::id()==$fd->user_id){
                $friends[] = User::where('id','=',$fd->fd_id)->get()[0];
            } else {
                $friends[] = User::where('id','=',$fd->user_id)->get()[0];
            }
        }
        return $friends;
    }

